define([
  'jasmine',
  'jasmine-html',
  'jasmine-console'
], function () {
  return function () {
    /**
     * ## Require &amp; Instantiate
     *
     * Require Jasmine's core files. Specifically, this requires and attaches all of Jasmine's code to the `jasmine` reference.
     */
    window.jasmine = jasmineRequire.core(jasmineRequire);

    /**
     * Since this is being run in a browser and the results should populate to an HTML page, require the HTML-specific Jasmine code, injecting the same reference.
     */
    jasmineRequire.html(jasmine);

    /**
     * Create the Jasmine environment. This is used to run all specs in a project.
     */
    var env = jasmine.getEnv();

    /**
     * ## The Global Interface
     *
     * Build up the functions that will be exposed as the Jasmine public interface. A project can customize, rename or alias any of these functions as desired, provided the implementation remains unchanged.
     */
    var jasmineInterface = jasmineRequire.interface(jasmine, env);

    /**
     * Add all of the Jasmine global/public interface to the proper global, so a project can use the public interface directly. For example, calling `describe` in specs instead of `jasmine.getEnv().describe`.
     */
    if (typeof window == "undefined" && typeof exports == "object") {
      extend(exports, jasmineInterface);
    } else {
      extend(window, jasmineInterface);
    }

    /**
     * ## Runner Parameters
     *
     * More browser specific code - wrap the query string in an object and to allow for getting/setting parameters from the runner user interface.
     */

    var queryString = new jasmine.QueryString({
      getWindowLocation: function() { return window.location; }
    });

    var catchingExceptions = queryString.getParam("catch");
    env.catchExceptions(typeof catchingExceptions === "undefined" ? true : catchingExceptions);


    var reporters = initReporters();

    /**
     * The `jsApiReporter` also receives spec results, and is used by any environment that needs to extract the results  from JavaScript.
     */
     addReporter();


    /**
     * Filter which specs will be run by matching the start of the full name against the `spec` query param.
     */
    var specFilter = new jasmine.HtmlSpecFilter({
      filterString: function() { return queryString.getParam("spec"); }
    });

    env.specFilter = function(spec) {
      return specFilter.matches(spec.getFullName());
    };

    /**
     * Setting up timing functions to be able to be overridden.
     * Certain browsers (Safari, IE 8, phantomjs) require this hack.
     */
    window.setTimeout    = window.setTimeout;
    window.setInterval   = window.setInterval;
    window.clearTimeout  = window.clearTimeout;
    window.clearInterval = window.clearInterval;

    /**
     * Helper function for readability above.
     */
    function extend(destination, source) {
      for (var property in source) destination[property] = source[property];
      return destination;
    }

    /**
     * ## Execution
     */
    return function () {
      reporters.html.initialize();
      env.execute();
    };
    function initReporters() {
      return {
        html : new jasmine.HtmlReporter({
          env                   : env,
          onRaiseExceptionsClick: function() { queryString.setParam("catch", !env.catchingExceptions()); },
          getContainer          : function() { return document.body; },
          createElement         : function() { return document.createElement.apply(document, arguments); },
          createTextNode        : function() { return document.createTextNode.apply(document, arguments); },
          timer                 : new jasmine.Timer()
        }),
        console : new jasmineRequire.ConsoleReporter()({
          showColors: true,
          timer     : new jasmine.Timer(),
          print     : function() {
            console.log.apply(console, arguments);
          }
        })
      };

    }
    function addReporter() {
      env.addReporter(jasmineInterface.jsApiReporter);
      env.addReporter(reporters.html);
      if (0 < navigator.userAgent.indexOf('PhantomJS')) {
        env.addReporter(reporters.console);
      }
    }
  };
});
